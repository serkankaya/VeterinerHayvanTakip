package net.serkankaya.vht.model;

public class SahipAta {
private long hid;
private long sid;


public SahipAta() {
}
public SahipAta(long hid, long sid) {
	super();
	this.hid = hid;
	this.sid = sid;
}
public long getHid() {
	return hid;
}
public void setHid(long hid) {
	this.hid = hid;
}
public long getSid() {
	return sid;
}
public void setSid(long sid) {
	this.sid = sid;
}




}
